const mongoose = require('mongoose');

const staffUserSchema = mongoose.Schema({
    // PROPERTIES OF A STUDENT USER
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});

module.exports = mongoose.model('staffUser', staffUserSchema);